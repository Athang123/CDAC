public class Q3 {
    public static void main(String[] args) {

        int i = 100;
        double d = i;  // int to double
        System.out.println("Implicit typecasting: " + d);
        
        long l = 100L;
        int i1 = (int) l;  // long to double
        System.out.println("Explicit typecasting: " + i1);
    }

}
